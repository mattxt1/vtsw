interface SimpleData {
  model: string;
  year: 2023 | 2024 | 2025;
  segmentId: ArchiveBase["segmentId"];
  groupName: string;
  tier?: string;
  platform?: string;
  support: string;
  status?: ArchiveStatus;
  successor?: string;
  note?: string;
  tagline: string;
  highlights: Pair[];
  groups: Array<[title: string, items: Pair[]]>;
}

const pair = (value: string, label: string): ProductHighlight => ({
  value,
  label,
});

const specs = (title: string, items: Array<Pair | undefined>): ProductSpecGroup => ({
  title,
  items: items.flatMap((item) =>
    item ? [{ label: item[0], value: item[1] }] : [],
  ),
});

const slug = (value: string) =>
  value
    .toLowerCase()
    .replace(/\+/g, "-plus")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");

function phone(data: PhoneData): ArchiveBase {
  return {
    model: data.model,
    year: data.year,
    segmentId: "mobile",
    groupName: data.groupName ?? "x series",
    tier: data.tier ?? "standard",
    platform: data.platform ?? `vOS ${String(data.year).slice(2)}`,
    support: data.support,
    status: data.status,
    successor: data.successor,
    note: data.note,
    tagline: data.tagline,
    highlights: [
      pair(data.brightness ?? data.display, "display"),
      pair(data.cameras.split(",")[0], "main camera"),
      pair(data.battery, "battery"),
      pair(data.chip.replace("vela pulse ", ""), "vela pulse"),
    ],
    specifications: [
      specs("Display", [
        ["Canvas", data.display],
        data.resolution ? ["Resolution", data.resolution] : undefined,
        ["Refresh rate", data.refresh],
        data.brightness ? ["Peak brightness", data.brightness] : undefined,
      ]),
      specs("Camera system", [
        ["Cameras", data.cameras],
        data.video ? ["Video", data.video] : undefined,
      ]),
      specs("Performance", [
        ["Chip", data.chip],
        ["Memory and storage", data.memory],
      ]),
      specs("Design and power", [
        data.materials ? ["Materials", data.materials] : undefined,
        data.protection ? ["Protection", data.protection] : undefined,
        ["Battery", data.battery],
        ["Charging", data.charging],
        data.dimensions ? ["Dimensions", data.dimensions] : undefined,
        data.weight ? ["Weight", data.weight] : undefined,
      ]),
    ],
    finishes: data.finishes,
  };
}

